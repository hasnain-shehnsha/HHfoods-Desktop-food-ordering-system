    private void txtDateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtDateActionPerformed
        // TODO add your handling code here:
        String dateInput = txtDate.getText().trim();
    
        if (dateInput.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter a date like 17-06-2025, 06-2025, or 2025");
            return;
        }

        String sqlQuery = "";

        try {
            // Full Date (dd-MM-yyyy)
            if (dateInput.matches("\\d{2}-\\d{2}-\\d{4}")) {
                sqlQuery = "SELECT * FROM bills WHERE date LIKE '%" + dateInput + "%'";
            }
            // Month-Year (MM-yyyy)
            else if (dateInput.matches("\\d{2}-\\d{4}")) {
                sqlQuery = "SELECT * FROM bills WHERE date LIKE '%-" + dateInput + "%'";
            }
            // Only Year
            else if (dateInput.matches("\\d{4}")) {
                sqlQuery = "SELECT * FROM bills WHERE date LIKE '%" + dateInput + "'";
            } 
            else {
                JOptionPane.showMessageDialog(null, "Invalid format. Use dd-MM-yyyy or MM-yyyy or yyyy.");
                return;
            }

            Connection con = connectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sqlQuery);

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0); // Clear previous data

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("purchaseID"),
                    rs.getString("name"),
                    rs.getString("mobileNumber"),
                    rs.getString("email"),
                    rs.getString("date"),
                    rs.getString("total"),
                    rs.getString("createdBy")
                });
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e);
        }
    }//GEN-LAST:event_txtDateActionPerformed

