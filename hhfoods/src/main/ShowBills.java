    public void tableDetails(){
        String date = txtDate.getText();
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        ArrayList<bills> list = billDao.getAllRecordsByInc(date);
        Iterator<bills> itr = list.iterator();
        while(itr.hasNext()){
            bills billobj = itr.next();
            dtm.addRow(new Object[]{billobj.getPurchaseID(), billobj.getName(), billobj.getMobileNumber(),billobj.getEmail(), billobj.getDate(),billobj.getTotal(),billobj.getCreatedBy()});
        }
        
    }
