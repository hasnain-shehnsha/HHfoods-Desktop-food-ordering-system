    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        // TODO add your handling code here:
        String username = tusername.getText();
        if (username.isEmpty()){
            JOptionPane.showMessageDialog(this, "Username Should Not be Empty.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
            user u=null;
                u = userDao.getSecurityQuestion(username);
                if (u==null){
                    JOptionPane.showMessageDialog(this, "<html><b style=\"color:red\">Incorrect Username.</b></html>", "Message", JOptionPane.ERROR_MESSAGE);

                }
                else{
                    
                    danswer = u.getAnswer();
                    trecq.setText(u.getSecurityQyestion());
                    tusername.setEditable(false);
                }
        }
    }//GEN-LAST:event_btnsearchActionPerformed

