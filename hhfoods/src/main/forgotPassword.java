    private void btnsubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsubmitActionPerformed
        // TODO add your handling code here:

        String username = tusername.getText();
        String password = tpassword.getText();
        String recq = trecq.getText().trim();
        String reca = tanswer.getText().trim();

        if(username.isEmpty() && password.isEmpty()&& recq.isEmpty() && reca.isEmpty()){
            JOptionPane.showMessageDialog(this, "Username and Password Should Not be Empty.", "Message", JOptionPane.ERROR_MESSAGE);

        }else if(username.isEmpty()){

            JOptionPane.showMessageDialog(this, "Username Should Not be Empty.", "Error", JOptionPane.ERROR_MESSAGE);

        }else if(password.isEmpty()){

            JOptionPane.showMessageDialog(this, "Password Should Not be Empty.", "Error", JOptionPane.ERROR_MESSAGE);

        }else if (recq.isEmpty()){
            
            JOptionPane.showMessageDialog(this, "RecQuestion is Empty.", "Error", JOptionPane.ERROR_MESSAGE);
            
        }else if(reca.isEmpty()){
            
            JOptionPane.showMessageDialog(this, "Answer is Empty.", "Error", JOptionPane.ERROR_MESSAGE);
            
        }else{
            if(reca.equals(danswer)){
                userDao.update(username, password);
                clear();
            }
            else{
                JOptionPane.showMessageDialog(this, "<html><b style=\"color:red\">Incorrect Answer.</b></html>", "Message", JOptionPane.ERROR_MESSAGE);
            }

        }

    }//GEN-LAST:event_btnsubmitActionPerformed

